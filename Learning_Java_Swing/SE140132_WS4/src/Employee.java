public class Employee extends AbstractEntity {
    private int salary;

    public Employee(String code, String name, int salary) {
        super(code, name);
        this.salary = salary;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }
    
}
