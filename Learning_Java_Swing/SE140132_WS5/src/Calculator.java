    private Double getResult(OperationEnum method, double... nums) {
        switch(method) {
            case PLUS:
                return nums[0] + nums[1];
            case DISTRACT:
                return nums[0] - nums[1];
            case MULTIPLY:
                return nums[0] * nums[1];
            case DIVISION:
                return nums[1] != 0 ? nums[0] / nums[1] : -1;
        }
        return 0d;
    }
    
    private String inputNum(String value) {
        return fdDisplay.getText() + value;
    }    
    
    private Optional<OperationEnum> renderInstance(String cmd) {
        return operList
                .stream()
                .filter(oper -> oper.getValue().equals(cmd))
                .findAny();
    }
    
    private Double getNum() {
        return Double.parseDouble(fdDisplay.getText());
    }
    
