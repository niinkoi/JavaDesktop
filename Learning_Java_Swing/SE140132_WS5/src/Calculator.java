    private void btnOneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOneActionPerformed
        String inputNum = evt.getActionCommand();
        if(isEqualPressed) {
            System.out.println(isEqualPressed);
            isEqualPressed = false;
            fdDisplay.setText(EMPTY);
        }
        fdDisplay.setText(inputNum(inputNum));
    }//GEN-LAST:event_btnOneActionPerformed

    private void btnPlusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlusActionPerformed
        instance = renderInstance(evt.getActionCommand()).get();
        if (firstNum == 0) {
           firstNum = getNum();
       } else {
           firstNum = getResult(instance, firstNum, getNum());
       }
        fdDisplay.setText(EMPTY);
    }//GEN-LAST:event_btnPlusActionPerformed

    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        secondNum += getNum();
        fdDisplay.setText(EMPTY);
        double result = getResult(instance, firstNum, secondNum);
        String output = EMPTY;
        if (instance == OperationEnum.DIVISION && result == -1.0) {
            output = MATH_ERROR;
        } else {
            output = String.valueOf(decimalFormat.format(getResult(instance, firstNum, secondNum)));
        }
        fdDisplay.setText(output);
        isEqualPressed = true;
        reset();
    }//GEN-LAST:event_btnEqualActionPerformed

