    private void btnSaveSupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveSupActionPerformed
        Suppliers suppliers = new Suppliers();
        suppliers.setCode(txtSupCode.getText());
        suppliers.setName(txtSupName.getText());
        suppliers.setAddress(txtAddress.getText());
        
        suppliers.setColloborating(checkCobol.isSelected());
        
        if (txtSupCode.isEditable() && !isFieldsEmpty(txtSupCode, txtSupName, txtAddress)) {
            try {
                supplierService.saveAction(this, suppliers);
            } catch (SQLException ex) {
                Logger.getLogger(ManagerForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (suppliers.getCode().isEmpty() || suppliers.getName().isEmpty() 
                || suppliers.getAddress().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fields can not be empty");
            return;
        } else {
            try {
                supplierService.updateAction(this, suppliers);
            } catch (SQLException e) {
                Logger.getLogger(ManagerForm.class.getName()).log(Level.SEVERE, null, e);
            }
        }
        supplierService.loadDataToTable(jTable1);
        itemService.loadDataToComboBox(supplierService.getList(), cbSuppliers);
    }//GEN-LAST:event_btnSaveSupActionPerformed

    private boolean isFieldsEmpty(JTextField fieldCode, JTextField fieldName, JTextField required) {
        return fieldCode.getText().isEmpty() || fieldName.getText().isEmpty() || required.getText().isEmpty();
    }
    
