    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        Items item = itemService.getClickedRow(jTable2);
        if (item == null) {
            JOptionPane.showMessageDialog(this, evt);
        } else {
            txtItemCode.setText(item.getCode());
            txtItemCode.setEditable(false);
            txtItemName.setText(item.getName());
            cbSuppliers.setSelectedItem(item.getSupplier().toString());
            txtUnit.setText(item.getUnit());
            txtPrice.setText(String.valueOf(item.getPrice()));
            checkSupplying.setSelected(item.isSupplying());
        }
    }//GEN-LAST:event_jTable2MouseClicked

