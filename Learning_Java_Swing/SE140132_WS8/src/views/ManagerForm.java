    private void btnSaveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveItemActionPerformed
        Items item = new Items();
        item.setCode(txtItemCode.getText());
        item.setName(txtItemName.getText());
        item.setSupplier(supplierService.getSupplierFromComboBox(cbSuppliers));
        item.setUnit(txtUnit.getText());
        item.setSupplying(checkSupplying.isSelected());
        
        if (txtItemCode.isEditable() && !isFieldsEmpty(txtItemCode, txtItemName, txtPrice)) {
            try {
                item.setPrice(Float.parseFloat(txtPrice.getText()));
                itemService.saveAction(this, item);
            } catch (SQLException | NumberFormatException ex ) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else if (isFieldsEmpty(txtItemCode, txtItemName, txtPrice)) {
            JOptionPane.showMessageDialog(this, "Fields can not be empty");
            return;
        } else {
            try {
                item.setPrice(Float.parseFloat(txtPrice.getText()));
                itemService.updateAction(this, item);
            } catch (SQLException | NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        itemService.loadDataToTable(jTable2);
    }//GEN-LAST:event_btnSaveItemActionPerformed

    private void btnDelItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelItemActionPerformed
        String code = txtItemCode.getText();
        try {
            itemService.deleteAction(code, this);
        } catch (Exception ex) {
            Logger.getLogger(ManagerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        itemService.loadDataToTable(jTable2);
        setEmptyField(txtItemCode, txtItemName, txtUnit, txtPrice);
        checkSupplying.setSelected(false);
    }//GEN-LAST:event_btnDelItemActionPerformed

